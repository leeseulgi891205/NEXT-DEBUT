CREATE TABLE IF NOT EXISTS MY_ITEM (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  MEMBER_ID BIGINT NOT NULL,
  ITEM_NAME VARCHAR(100) NOT NULL,
  QUANTITY INT NOT NULL DEFAULT 1,
  CONSTRAINT FK_MY_ITEM_MEMBER FOREIGN KEY (MEMBER_ID) REFERENCES MEMBER(MNO)
);

CREATE UNIQUE INDEX IF NOT EXISTS UK_MY_ITEM_MEMBER_NAME
ON MY_ITEM (MEMBER_ID, ITEM_NAME);

-- market transaction log (buy/admin adjust)
CREATE TABLE IF NOT EXISTS MARKET_TXN (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  MEMBER_ID BIGINT NOT NULL,
  TXN_TYPE VARCHAR(30) NOT NULL,         -- BUY / ADMIN_ADJUST
  ITEM_NAME VARCHAR(100),               -- BUY일 때 사용
  COIN_DELTA INT NOT NULL DEFAULT 0,    -- -price, +grant 등
  NOTE VARCHAR(255),
  CREATED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT FK_MARKET_TXN_MEMBER FOREIGN KEY (MEMBER_ID) REFERENCES MEMBER(MNO)
);

CREATE INDEX IF NOT EXISTS IX_MARKET_TXN_MEMBER_CREATED
ON MARKET_TXN (MEMBER_ID, CREATED_AT);

-- 회원 보유 연습생 (가챠·향후 로스터 연동)
CREATE TABLE IF NOT EXISTS MY_TRAINEE (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  MEMBER_ID BIGINT NOT NULL,
  TRAINEE_ID BIGINT NOT NULL,
  QUANTITY INT NOT NULL DEFAULT 1,
  ENHANCE_LEVEL INT NOT NULL DEFAULT 0,
  CONSTRAINT FK_MY_TRAINEE_MEMBER FOREIGN KEY (MEMBER_ID) REFERENCES MEMBER(MNO),
  CONSTRAINT FK_MY_TRAINEE_TRAINEE FOREIGN KEY (TRAINEE_ID) REFERENCES TRAINEE(ID)
);

CREATE UNIQUE INDEX IF NOT EXISTS UK_MY_TRAINEE_MEMBER_TRAINEE
ON MY_TRAINEE (MEMBER_ID, TRAINEE_ID);

CREATE INDEX IF NOT EXISTS IX_MY_TRAINEE_MEMBER ON MY_TRAINEE (MEMBER_ID);

-- 가챠 결과 로그 (회원별 뽑기 이력·운영 분석)
CREATE TABLE IF NOT EXISTS GACHA_PULL_LOG (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  MEMBER_ID BIGINT NOT NULL,
  TRAINEE_ID BIGINT NOT NULL,
  GRADE VARCHAR(10),
  POOL_ID VARCHAR(32) NOT NULL DEFAULT 'DEFAULT',
  CREATED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT FK_GACHA_PULL_MEMBER FOREIGN KEY (MEMBER_ID) REFERENCES MEMBER(MNO)
);

CREATE INDEX IF NOT EXISTS IX_GACHA_PULL_MEMBER_CREATED ON GACHA_PULL_LOG (MEMBER_ID, CREATED_AT);

-- 캐스팅 맵 스팟 버프 (탐색으로 획득, 만료 시 가챠 미적용)
CREATE TABLE IF NOT EXISTS CASTING_SPOT_BUFF (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  MEMBER_ID BIGINT NOT NULL,
  REGION_CODE VARCHAR(40),
  SPOT_LABEL VARCHAR(120) NOT NULL,
  EFFECT_TYPE VARCHAR(40) NOT NULL,
  EFFECT_VALUE VARCHAR(80),
  EXPIRE_AT TIMESTAMP NOT NULL,
  CREATED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT FK_CASTING_SPOT_BUFF_MEMBER FOREIGN KEY (MEMBER_ID) REFERENCES MEMBER(MNO)
);

CREATE INDEX IF NOT EXISTS IX_CASTING_SPOT_BUFF_MEMBER_EXPIRE
ON CASTING_SPOT_BUFF (MEMBER_ID, EXPIRE_AT);

-- 캐스팅 맵 일일 탐색 횟수 (무료 3회 초과 시 유료)
CREATE TABLE IF NOT EXISTS CASTING_MAP_DAILY_EXPLORE (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  MEMBER_ID BIGINT NOT NULL,
  EXPLORE_DATE DATE NOT NULL,
  EXPLORE_COUNT INT NOT NULL DEFAULT 0,
  CONSTRAINT UK_CASTING_MAP_DAILY_MEMBER_DAY UNIQUE (MEMBER_ID, EXPLORE_DATE),
  CONSTRAINT FK_CASTING_MAP_DAILY_MEMBER FOREIGN KEY (MEMBER_ID) REFERENCES MEMBER(MNO)
);

